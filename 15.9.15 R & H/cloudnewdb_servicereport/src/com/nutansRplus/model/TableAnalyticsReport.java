package com.nutansRplus.model;

public class TableAnalyticsReport {
private String tablename;
private int costlytable;
private int lasttable;
public String getTablename() {
	return tablename;
}
public void setTablename(String tablename) {
	this.tablename = tablename;
}
public int getCostlytable() {
	return costlytable;
}
public void setCostlytable(int costlytable) {
	this.costlytable = costlytable;
}
public int getLasttable() {
	return lasttable;
}
public void setLasttable(int lasttable) {
	this.lasttable = lasttable;
}

}
